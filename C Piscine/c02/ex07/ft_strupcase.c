char	*ft_strupcase(char *str)
{
	int		i;
	char	*p;

	i = 0;
	while (*(str + i) != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] -= 32;
		}
		i++;
	}
	p = str;
	return (p);
}
