int	ft_str_is_printable(char *str)
{
	int	i;
	int	f;

	i = 0;
	f = 0;
	while (*(str + i) != '\0')
	{
		f = (int) str[i];
		if (f <= 31 || f == 127 || f == 152 || f == 160 || f == 173)
		{
			return (0);
		}
		i++;
	}
	if (f == 0)
		return (0);
	else
		return (1);
}
