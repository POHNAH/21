int	ft_str_is_numeric(char *str)
{
	int	i;
	int	f;

	i = 0;
	f = 0;
	while (*(str + i) != '\0')
	{
		f = 1;
		if (str[i] < '0' || str[i] > '9')
		{
			return (0);
		}
		i++;
	}
	if (f == 0)
		return (0);
	else
		return (1);
}
