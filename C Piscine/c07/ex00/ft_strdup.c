#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*d;

	i = 0;
	while (*(src + i) != '\0')
		++i;
	d = (char *) malloc(i + 1);
	while (i > -1)
	{
		d[i] = src[i];
		--i;
	}
	return (d);
}
