#include <unistd.h>

void	print_number(int n)
{
	char	c;

	if (n / 10 > 0)
		print_number(n / 10);
	else if (n < 0)
	{
		c = '-';
		write(1, &c, 1);
	}
	if (n < 0)
		c = '0' - n % 10;
	else
		c = '0' + n % 10;
	write(1, &c, 1);
}
