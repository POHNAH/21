void	ft_swap(int *a, int *b)
{
	if (*a == 0)
	{
		*a = *b;
		*b = 0;
	}
	else if (*b == 0)
	{
		*b = *a;
		*a = 0;
	}
	else
	{
		*a += *b;
		*b = *a - *b;
		*a -= *b;
	}
}
